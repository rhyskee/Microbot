package net.runelite.client.plugins.microbot.util.grandexchange;

/**
 * Represents the possible actions that can be performed on the Grand Exchange.
 */
public enum GrandExchangeAction {
	/** Represents a buy offer action. */
	BUY,

	/** Represents collecting completed offers or items. */
	COLLECT,

	/** Represents a sell offer action. */
	SELL
}
